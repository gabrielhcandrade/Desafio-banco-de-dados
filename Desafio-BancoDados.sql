-- Número 1: Buscar nome e ano dos filmes

SELECT 
	Nome,
	Ano
FROM Filmes

-- Número 2: Buscar o nome e ano dos filmes, ordenados por ordem crescente pelo ano

SELECT
	Nome,
	ANO,
	Duracao
FROM Filmes
ORDER BY Ano ASC

-- Número 3: Buscar pelo filme de volta para o futuro, trazendo o nome, ano e a duração

SELECT
	Nome,
	Ano,
	Duracao
FROM Filmes WHERE Nome = 'De volta para o futuro'

-- Número 4: Buscar os filmes lançados em 1997

SELECT
	Nome,
	Ano,
	Duracao
FROM Filmes WHERE Ano = 1997

-- Número 5: Buscar os filmes lançados após os anos 2000

SELECT 
	Nome,
	Ano,
	Duracao
FROM Filmes 
WHERE Ano >= 2000

-- Número 6: Buscar os filmes com a duração maior que 100 e menor que 150, ordenando pela duração em ordem crescente

SELECT
	Nome,
	Ano,
	Duracao
FROM Filmes
WHERE Duracao > 100 AND Duracao < 150
ORDER BY Duracao ASC

-- Número 7: Buscar a quantidade de filmes lançados no ano, agrupando por ano, ordenando pela duração em ordem decrescente

SELECT 
	Ano,
	COUNT(*) Quantidade
FROM Filmes 
WHERE Ano <> ' '
GROUP BY Ano
ORDER BY Quantidade DESC

-- Número 8:  Buscar os Atores do gênero masculino, retornando o PrimeiroNome, UltimoNome

SELECT * FROM Atores
WHERE Genero = 'M'

-- Número 9: Buscar os Atores do gênero feminino, retornando o PrimeiroNome, UltimoNome, e ordenando pelo PrimeiroNome

SELECT * FROM Atores
WHERE Genero = 'F'
ORDER BY PrimeiroNome

-- Número 10:  Buscar o nome do filme e o gênero

SELECT 
	Nome,
	Genero
FROM
	Filmes 
INNER JOIN FilmesGenero ON Filmes.Id = FilmesGenero.IdFilme
INNER JOIN Generos ON Generos.Id = FilmesGenero.IdGenero

-- Número 11: Buscar o nome do filme pelo gênero 'Mistério'

SELECT 
	Nome,
	Genero
FROM
	Filmes
INNER JOIN FilmesGenero ON Filmes.Id = FilmesGenero.IdFilme
INNER JOIN Generos ON Generos.Id = FilmesGenero.IdGenero
WHERE Genero = 'Mistério'

-- Número 12: Buscar o nome do filme e os atores, trazendo o PrimeiroNome, UltimoNome e seu Papel

SELECT 
	Nome,
	PrimeiroNome,
	UltimoNome,
	Papel
FROM 
	Filmes
INNER JOIN ElencoFilme ON Filmes.Id = ElencoFilme.IdFilme
INNER JOIN Atores ON Atores.Id = ElencoFilme.IdAtor


